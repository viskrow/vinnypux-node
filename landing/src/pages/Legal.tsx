function Requisites() {
  const rows: [string, string][] = [
    ['Полное наименование', 'Общество с ограниченной ответственностью «Стримора»'],
    ['Сокращённое наименование', brand.legalName],
    ['Генеральный директор', brand.director],
    ['ИНН', brand.inn],
    ['КПП', brand.kpp],
    ['ОГРН', brand.ogrn],
    ['Юридический адрес', brand.address],
    ['Расчётный счёт', brand.account],
    ['Банк', brand.bank],
    ['БИК', brand.bik],
    ['Корр. счёт', brand.corrAccount],
    ['E-mail', brand.emailSales],
    ['Телефон', brand.phone],
  ]
  return (
    <Table verticalSpacing="sm" withRowBorders>
      <Table.Tbody>
        {rows.map(([k, v]) => (
          <Table.Tr key={k}>
            <Table.Td c="dimmed" w={{ base: 160, sm: 240 }} style={{ verticalAlign: 'top' }}>
              {k}
            </Table.Td>
            <Table.Td fw={500}>{v}</Table.Td>
          </Table.Tr>
        ))}
      </Table.Tbody>
    </Table>
  )
}

export default function Legal() {
  const { doc } = useParams()

  if (doc === 'requisites') {
    return (
      <PageShell size="md">
        <PageHeader title="Реквизиты" subtitle="Реквизиты для заключения договора и оплаты услуг." />
        <Requisites />
      </PageShell>
    )
  }

  const d = doc ? docs[doc] : undefined
  if (!d) return <NotFound />

  return (
    <PageShell size="md">
      <PageHeader title={d.title} />
      <Text c="dimmed" fz="sm" mb="xl">
        Редакция от {d.updated}
      </Text>
      {d.intro && (
        <Text c="dimmed" fz="lg" lh={1.75} mb={40}>
          {d.intro}
        </Text>
      )}
      <Stack gap={32}>
        {d.sections.map((s) => (
          <Stack key={s.h} gap="xs">
            <Title order={3} fz={{ base: 17, sm: 20 }}>
              {s.h}
            </Title>
            {s.p.map((para, i) => (
              <Text key={i} c="dimmed" lh={1.7}>
                {para}
              </Text>
            ))}
          </Stack>
        ))}
      </Stack>
    </PageShell>
  )
}
